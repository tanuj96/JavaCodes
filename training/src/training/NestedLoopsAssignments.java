package training;

public class NestedLoopsAssignments {
		public static void main(String[] args)
		{
		int n=1;
		for(int i=1;i<=1;i++)
		{
		for(int j=1;j<=1;j++)
		  {
			System.out.println(n);
		  }
		for(int k=1;k<=2;k++)
		{
			System.out.print(n  + "  ");
			n++;
		}
		System.out.println();
		n=1;
		for(int l=1;l<=3;l++)
		{
			System.out.print(n  + "  ");
			n++;
		}
		System.out.println();
		n=1;
		for(int m=1;m<=4;m++)
		{
			System.out.print(n  + "  ");
			n++;
		}
		System.out.println();
		n=1;
		for(int o=1;o<=5;o++)
		{
			System.out.print(n  + "  ");
			n++;
		}
		}
}
}